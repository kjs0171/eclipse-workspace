package chapter02;

public class VariableEx01 {
	
	public static void main(String[] args) {
		
		int level;      //변수를 선언 후, 값을 할당.
		level = 10;
		level = 50;
		level = 1000;
				
		int level2 = 20;   //변수 선언과 동시에 값을 할당.
		level2 = 100;
		level2 = 2000;
		
		System.out.println(level);
		System.out.println(level2);
		
		
		
	}
}
