package chapter02;

public class CharacterEX01 {
	
	public static void main(String[] args){
		
		char ch1;
		 ch1 = 'A';
		System.out.println(ch1);		//문자 출력
		System.out.println((int)ch1);	//아스키 코드 출력
		
		char ch2 = 66;
		System.out.println(ch2);
		
		int ch3 = 67;
		System.out.println(ch3);
		System.out.println((char)ch3);
		
	}
}
